package javapackage;

public class If_Else_If_Demo {

	public static void main(String[] args) {

		int j = 800;
		int p = 900;

		if (j < p) {

			System.out.println("j is smaller");
		}

		else if (j == p) {
			System.out.println("j is equal");
		}

		else {

			System.out.println("j is greater");
		}
	}
}
