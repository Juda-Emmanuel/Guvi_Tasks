package javapackage;

public class Arithmetic_Operations_Demo {

	public static void main(String[] args) {

		// Addition :

		int a = 10;
		int b = 20;

		int add = a + b;

		System.out.println("Sum of two numbers are " + add);

		// Subtraction :

		int c = 10;
		int d = 20;

		int sub = d - c;

		System.out.println("Subtraction of two numbers are " + sub);

		// Multiple :

		int e = 5;
		int f = 10;

		int multiple = f * e;

		System.out.println("Multiple of two numbers are " + multiple);
		
		// Divisible :
		
		int g = 20;
		int h = 10;
		
		int divisible =g / h;
		
		System.out.println("Divisible of two numbers are " + divisible);
	}
}
